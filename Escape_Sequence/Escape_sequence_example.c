#include <stdio.h> // Include standard input/output library
int main() {       // Start of main() function
    
    printf("Hello, World!\n"); 
    printf("Hello!\n");         // \n: Newline
    
    printf("\"I am Shin Hee-jang.\"\n"); // \": Print double quotes
    
    printf("\\n means a newline.\n");    // \\: Print backslash
    
    printf("\tTab\tTest\n");             // \t: Print tab character
    
    printf("Backslash: \\, Quote: \", Tab: \t\n"); // Multiple escape sequences
    
    return 0;

} // End of main() function